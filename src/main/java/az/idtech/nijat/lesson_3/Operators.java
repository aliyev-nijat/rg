package az.idtech.nijat.lesson_3;

public class Operators {

    public static void main(String[] args) {
        int a = 15;
        int b = 4;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        float x = 7.5f;
        float y = 2.0f;
        System.out.println(x / y);

        a = 10;
        b = 5;
        int c = 10;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a == c);
        System.out.println(a != b);
        System.out.println(b >= a);
        
    }
}
